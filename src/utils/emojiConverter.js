  const textToEmoji = (text) => {
    return text
      .split(" ")
      .map((word) => emojiMap[word.toLowerCase()] || word) // Handling lowercase for words
      .join(" ");
  };
  
  const emojiToText = (emojiString) => {
    const emojiToTextMap = Object.fromEntries(
      Object.entries(emojiMap).map(([key, value]) => [value, key])
    );
    return emojiString
      .split(" ")
      .map((emoji) => emojiToTextMap[emoji] || emoji) // Handling emoji mapping
      .join(" ");
  };
  
